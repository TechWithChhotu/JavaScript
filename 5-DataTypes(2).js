// Object Data Types => The object data type can contain:
// # An object
// # An array
// # A date

// An Object =>
let person = {
  name: "Chhotu",
  age: 19,
};
console.log(person);
console.log("Type of person var => ", typeof person);

// # An Array
let numbers = [5, 9, 8, 2, 4];
console.log(numbers);
console.log("Type of numbers var => ", typeof numbers);

// # A date
const todayDate = new Date();
console.log(todayDate);
console.log("Type of todayDate var => ", typeof todayDate);
